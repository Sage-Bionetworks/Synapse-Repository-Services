package org.sagebionetworks.auth;

public class ResourceAccessToken {
	private String resourceAccessToken;

	/**
	 * @return the token
	 */
	public String getResourceAccessToken() {
		return resourceAccessToken;
	}

	/**
	 * @param token the token to set
	 */
	public void setResourceAccessToken(String token) {
		this.resourceAccessToken = token;
	}
}
