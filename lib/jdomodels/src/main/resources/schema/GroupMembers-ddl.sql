CREATE TABLE IF NOT EXISTS `GROUP_MEMBERS` (
  `GROUP_ID` BIGINT NOT NULL,
  `MEMBER_ID` BIGINT NOT NULL,
  CONSTRAINT `UNIQUE_MEMBERSHIP` PRIMARY KEY (`GROUP_ID`, `MEMBER_ID`), 
  CONSTRAINT `GROUP_ID_FK` FOREIGN KEY (`GROUP_ID`) REFERENCES `USER_GROUP` (`ID`) ON DELETE CASCADE, 
  CONSTRAINT `MEMBER_ID_FK` FOREIGN KEY (`MEMBER_ID`) REFERENCES `USER_GROUP` (`ID`) ON DELETE CASCADE 
)
