CREATE TABLE IF NOT EXISTS JDOEVALUATION (
    ID BIGINT NOT NULL,
    ETAG char(36) NOT NULL,
    NAME varchar(256)CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL,
    DESCRIPTION mediumblob DEFAULT NULL,
    OWNER_ID BIGINT NOT NULL,
    CREATED_ON BIGINT NOT NULL,
    CONTENT_SOURCE BIGINT NOT NULL,
    STATUS int DEFAULT NULL, -- deprecated column remove after stack-347
    SUBMISSION_INSTRUCTIONS_MESSAGE mediumblob,
    SUBMISSION_RECEIPT_MESSAGE mediumblob,
    QUOTA mediumblob,
    START_TIMESTAMP BIGINT,
    END_TIMESTAMP BIGINT,
    PRIMARY KEY (ID),
    UNIQUE KEY  JDOEVALUATION_U1 (NAME),
    FOREIGN KEY (OWNER_ID) REFERENCES JDOUSERGROUP (ID),
    FOREIGN KEY (CONTENT_SOURCE) REFERENCES JDONODE (ID) ON DELETE CASCADE
);
