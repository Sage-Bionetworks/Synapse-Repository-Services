CREATE TABLE IF NOT EXISTS EVALUATION_ROUNDS (
    ID BIGINT NOT NULL,
    ETAG char(36) NOT NULL,
    EVALUATION_ID BIGINT NOT NULL,
    ROUND_START DATETIME(3) NOT NULL,
    ROUND_END DATETIME(3) NOT NULL,
    LIMITS JSON,

    PRIMARY KEY (ID),
    INDEX `ROUND_START_INDEX` (ROUND_START),
    INDEX `ROUND_END_INDEX` (ROUND_END),
    # an index is implicitly created for foreign key
    CONSTRAINT `EVALUATION_ROUNDS_EVALUATION_ID_FK` FOREIGN KEY (`EVALUATION_ID`) REFERENCES `JDOEVALUATION` (`ID`) ON DELETE CASCADE
);
