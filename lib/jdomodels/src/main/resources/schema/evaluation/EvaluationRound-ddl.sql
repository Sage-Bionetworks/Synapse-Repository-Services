CREATE TABLE IF NOT EXISTS JDOEVALUATIONROUNDS (
    ID BIGINT NOT NULL,
    ETAG char(36) NOT NULL,
    EVALUATION_ID BIGINT NOT NULL,
    ROUND_START BIGINT NOT NULL,
    ROUND_END BIGINT NOT NULL,
    LIMITS JSON,

    PRIMARY KEY (ID),
    INDEX (ROUND_START),
    INDEX (ROUND_END),
    FOREIGN KEY (EVALUATION_ID) REFERENCES JDOEVALUATION (ID) ON DELETE CASCADE
);
