CREATE TABLE IF NOT EXISTS JDOPARTICIPANT (
    PARTICIPANT_ID bigint(20) NOT NULL,
    USER_ID bigint(20) NOT NULL,
    EVALUATION_ID bigint(20) NOT NULL,
    CREATED_ON bigint(20) NOT NULL,
    PRIMARY KEY PK_PARTICIPANT_ID (PARTICIPANT_ID),
    UNIQUE KEY PARTICIPANT_USER_EVAL_UNIQUE (USER_ID, EVALUATION_ID),
    FOREIGN KEY (EVALUATION_ID) REFERENCES JDOEVALUATION (ID) ON DELETE CASCADE,
    FOREIGN KEY (USER_ID) REFERENCES JDOUSERGROUP (ID) ON DELETE CASCADE
);
