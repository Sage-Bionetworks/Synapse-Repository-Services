CREATE TABLE JDOSUBMISSION_FILE (
    SUBMISSION_ID bigint(20) NOT NULL,
    FILE_HANDLE_ID bigint(20) NOT NULL,
    PRIMARY KEY ID (SUBMISSION_ID, FILE_HANDLE_ID),
    FOREIGN KEY (SUBMISSION_ID) REFERENCES JDOSUBMISSION (ID) ON DELETE CASCADE,
    FOREIGN KEY (FILE_HANDLE_ID) REFERENCES FILES (ID) ON DELETE RESTRICT
);