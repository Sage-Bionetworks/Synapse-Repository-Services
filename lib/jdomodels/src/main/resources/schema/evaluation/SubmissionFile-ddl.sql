CREATE TABLE IF NOT EXISTS EVALUATION_SUBMISSION_FILE (
    SUBMISSION_ID BIGINT NOT NULL,
    FILE_HANDLE_ID BIGINT NOT NULL,
    PRIMARY KEY ID (SUBMISSION_ID, FILE_HANDLE_ID),
    FOREIGN KEY (SUBMISSION_ID) REFERENCES EVALUATION_SUBMISSION (ID) ON DELETE CASCADE,
    FOREIGN KEY (FILE_HANDLE_ID) REFERENCES FILES (ID) ON DELETE RESTRICT
);
