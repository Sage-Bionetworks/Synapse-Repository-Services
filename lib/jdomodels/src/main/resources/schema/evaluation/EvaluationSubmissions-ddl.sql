CREATE TABLE IF NOT EXISTS `EVALUATION_SUBMISSIONS` (
  `ID` bigint NOT NULL,
  `ETAG` char(36) NOT NULL,
  `EVAL_ID` bigint NOT NULL,
  PRIMARY KEY (`ID`),
  KEY `EVALUATION_S_EVAL_ID_FK` (`EVAL_ID`),
  CONSTRAINT `EVALUATION_S_EVAL_ID_FK` FOREIGN KEY (`EVAL_ID`) REFERENCES `EVALUATION` (`ID`) ON DELETE CASCADE
)