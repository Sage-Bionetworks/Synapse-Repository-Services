CREATE TABLE IF NOT EXISTS `MESSAGE_CONTENT` (
  `ID` BIGINT NOT NULL,
  `CREATED_BY` BIGINT NOT NULL,
  `FILE_HANDLE_ID` BIGINT NOT NULL,
  `CREATED_ON` BIGINT NOT NULL,
  `ETAG` char(36) NOT NULL, 
  PRIMARY KEY (`ID`),
  CONSTRAINT `MESSAGE_CONTENT_CREATED_BY_FK` FOREIGN KEY (`CREATED_BY`) REFERENCES `USER_GROUP` (`ID`) ON DELETE CASCADE,
  CONSTRAINT `MESSAGE_CONTENT_FILE_HANDLE_ID_FK` FOREIGN KEY (`FILE_HANDLE_ID`) REFERENCES `FILES` (`ID`)
)
