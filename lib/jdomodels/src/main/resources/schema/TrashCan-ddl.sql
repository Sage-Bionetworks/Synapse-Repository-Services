CREATE TABLE `TRASH_CAN` (
	`TRASH_ID`    bigint(20)  NOT NULL AUTO_INCREMENT,
	`NODE_ID`     bigint(20)  NOT NULL,
	`DELETED_BY`  bigint(20)  NOT NULL,
	`DELETED_ON`  TIMESTAMP   NOT NULL,
	PRIMARY KEY (`ID`),
	UNIQUE `TRASH_CAN_NODE_ID_UNIQUE` (`NODE_ID`),
	CONSTRAINT `TRASH_CAN_DELETED_BY_FK` FOREIGN KEY (`DELETED_BY`) REFERENCES `JDOUSERGROUP` (`ID`),
	INDEX (`DELETED_ON`)
)
