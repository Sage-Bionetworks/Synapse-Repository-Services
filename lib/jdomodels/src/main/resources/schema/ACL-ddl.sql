CREATE TABLE IF NOT EXISTS `ACL` (
  `ID` BIGINT NOT NULL,
  `OWNER_ID` BIGINT NOT NULL,
  `OWNER_TYPE`  ENUM('ENTITY', 'EVALUATION', 'TEAM','FORM_GROUP','ORGANIZATION', 'ACCESS_REQUIREMENT'),
  `ETAG` char(36) NOT NULL,
  `CREATED_ON` BIGINT NOT NULL,
  PRIMARY KEY (`OWNER_ID`, `OWNER_TYPE`),
  INDEX `ACL_OWNER_TYPE_INDEX` (`OWNER_TYPE`),
  UNIQUE KEY `ACL_BACK_UP_ID` (`ID`)
)
