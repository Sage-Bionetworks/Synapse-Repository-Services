CREATE TABLE IF NOT EXISTS `V2_WIKI_PAGE` (
  `ID` bigint NOT NULL,
  `ETAG` char(36) NOT NULL,
  `TITLE` varchar(256) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `CREATED_BY` bigint NOT NULL,
  `CREATED_ON` bigint NOT NULL,
  `MODIFIED_BY` bigint NOT NULL,
  `MODIFIED_ON` bigint NOT NULL,
  `PARENT_ID` bigint DEFAULT NULL,
  `ROOT_ID` bigint NOT NULL,
  `MARKDOWN_VERSION` bigint NOT NULL,
  PRIMARY KEY (`ID`),
  KEY `V2_WIKI_CREATED_BY_FK` (`CREATED_BY`),
  KEY `V2_WIKI_MODIFIED_BY_FK` (`MODIFIED_BY`),
  KEY `V2_WIKI_PARENT_FK` (`PARENT_ID`),
  KEY `V2_WIKI_ROOT_FK` (`ROOT_ID`),
  CONSTRAINT `V2_WIKI_CREATED_BY_FK` FOREIGN KEY (`CREATED_BY`) REFERENCES `USER_GROUP` (`ID`) ON DELETE CASCADE,
  CONSTRAINT `V2_WIKI_MODIFIED_BY_FK` FOREIGN KEY (`MODIFIED_BY`) REFERENCES `USER_GROUP` (`ID`) ON DELETE CASCADE,
  CONSTRAINT `V2_WIKI_PARENT_FK` FOREIGN KEY (`PARENT_ID`) REFERENCES `V2_WIKI_PAGE` (`ID`) ON DELETE CASCADE,
  CONSTRAINT `V2_WIKI_ROOT_FK` FOREIGN KEY (`ROOT_ID`) REFERENCES `V2_WIKI_PAGE` (`ID`) ON DELETE CASCADE
)