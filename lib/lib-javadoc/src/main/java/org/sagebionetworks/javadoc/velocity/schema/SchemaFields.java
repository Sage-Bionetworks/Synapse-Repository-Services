package org.sagebionetworks.javadoc.velocity.schema;

public class SchemaFields {
	
	TypeReference type;
	String name;
	String description;
	
	public SchemaFields(){
	}
	public SchemaFields(TypeReference type, String name, String description) {
		super();
		this.type = type;
		this.name = name;
		this.description = description;
	}
	public TypeReference getType() {
		return type;
	}
	public void setType(TypeReference type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((description == null) ? 0 : description.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SchemaFields other = (SchemaFields) obj;
		if (description == null) {
			if (other.description != null)
				return false;
		} else if (!description.equals(other.description))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "SchemaFields [type=" + type + ", name=" + name
				+ ", description=" + description + "]";
	}
}
